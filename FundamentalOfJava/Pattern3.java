package FundamentalOfJava;
public class Pattern3 {
    public static void main (String args[]) {
        for (int i = 1; i  <= 5; i++) { // Code of number of rows
            for  (int j = 1; j  <= i; j++) { // Code for number of items to be printed in a row
                 System.out.print( "*"); 
            }
             System.out.println();
        }
    }
}
